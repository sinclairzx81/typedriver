import { compile, type Static } from 'typedriver'


// ------------------------------------------------------------------
// Compile
// ------------------------------------------------------------------
const Vector3 = compile(`{
  x: number
  y: number
  z: number
}`)

// ------------------------------------------------------------------
// Infer
// ------------------------------------------------------------------
type Vector3 = Static<typeof Vector3>

// ------------------------------------------------------------------
// Parse
// ------------------------------------------------------------------
const position = Vector3.parse({
  x: 0,
  y: 1,
  z: 0
})

console.log(position)